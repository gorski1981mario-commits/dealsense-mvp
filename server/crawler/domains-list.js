  /**
   * Get domain by name
   */
  getDomain(domainName) {
    // Search in giganci
    for (const category in this.giganci) {
      if (this.giganci[category].includes(domainName)) {
        return { domain: domainName, type: 'gigant', category }
      }
    }
    
    // Search in niszowe
    for (const category in this.niszowe) {
      if (this.niszowe[category].includes(domainName)) {
        return { domain: domainName, type: 'niszowy', category }
      }
    }
    
    return null
  },

  /**
   * Get 50/50 mix of domains for search
   */
  get5050Mix(category, count = 10) {
    const gigantCategory = this.giganci[category] || []
    const niszowyCategory = this.niszowe[`${category}_niche`] || []
    
    const halfCount = Math.floor(count / 2)
    const giganci = gigantCategory.slice(0, halfCount)
    const niszowe = niszowyCategory.slice(0, count - halfCount)
    
    return [...giganci, ...niszowe]
  },

  /**
   * Get total count
   */
  getTotalCount() {
    let total = 0
    
    for (const category in this.giganci) {
      total += this.giganci[category].length
    }
    
    for (const category in this.niszowe) {
      total += this.niszowe[category].length
    }
    
    return total
  }
